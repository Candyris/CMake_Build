#include "math.hpp"
namespace CandyMath
{
    int add(int first, int second)
    {
        return (first + second);
    }
    int sub(int first, int second)
    {
        return (first - second);
    }
    int mult(int first, int second)
    {
        return (first * second);
    }
    int div(int first, int second)
    {
        return (first / second);
    }
};